/* Function: mdlOutputs =======================================================
 * Abstract:
 *    In this function, you compute the outputs of your S-function
 *    block.
 */
static void mdlOutputs(SimStruct *S, int_T tid)
{
    int_T       *y = ssGetOutputPortSignal(S,0);
    y[0] = sock;
}


